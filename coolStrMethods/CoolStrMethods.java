package coolStrMethods;

public class CoolStrMethods {
    public static void main(String[] args) {
        // String in Java is a reference type that can store one or more characters
        // Reference types have access to useful methods

        String name = "Saul";
        // Always keep an eye in reference types in Java, this can save you some time
        boolean result = name.equals("Saul");

        System.out.println(result);
    }
}
